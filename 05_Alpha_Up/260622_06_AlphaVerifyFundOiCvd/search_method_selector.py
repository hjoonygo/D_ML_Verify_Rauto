# -*- coding: utf-8 -*-
# [search_method_selector.py] 가격레벨 탐색(체결검증 등) 방법 자동선택 — 캡틴 지시 2026-06-22.
#   "1m 전수 스캔(브루트포스)" vs "7h→1h→15m→1m 계층 캐스케이드" 중 상황별 빠른 쪽 선택.
#
# ★실측 크로스오버 (Merged_Data 1.58M 1m봉, 이 PC·pandas 기준, 윈도당 40회 평균):
#   윈도(1m봉)  브루트(ms)  캐스케이드(ms)  승자
#     1,000        0.25        1.08        브루트
#     20,000       0.38        1.15        브루트
#    300,000(0.6년) 0.60        1.36        브루트
#   ★500,000(1.0년) 1.45        1.33        캐스케이드  ← 역전
#    900,000(1.7년) 2.24        1.28        캐스케이드(1.8x)
#   1,000,000      2.70        1.20        캐스케이드(2.3x)
#  → 형태: 캐스케이드≈상수(~1.3ms, 윈도무관·드릴+조기반환) / 브루트≈O(W). 역전점 W*≈40만봉.
#  → 봉검사 횟수는 캐스케이드가 항상 ~148배 적음(99.3%↓). 단 pandas 슬라이스 오버헤드 때문에
#    작은 윈도에선 벡터연산 브루트가 시간상 더 빠름. numpy 구현이면 W* 더 왼쪽(캐스케이드 더 일찍 승).
#
# ★경계 단일값: W* ≈ 400,000 1m봉 ≈ 약 9개월(0.8년)분 1m.

W_STAR = 400_000   # 1m봉 (≈0.8년). 하드웨어·구현 의존이나 봇설계 판단엔 이 값으로 충분.


def pick_search_method(window_bars, fits_in_ram=True):
    """한 번의 탐색이 훑는 '최소TF봉 수'(window_bars)로 방법 선택.
    fits_in_ram=False(틱데이터·초대용량 등 전부 벡터적재 불가)면 무조건 캐스케이드(메모리 경계)."""
    if not fits_in_ram:
        return "cascade"            # 메모리 경계: 브루트(전적재)가 불가 → 계층/인덱스 필수
    return "brute" if window_bars < W_STAR else "cascade"


# ───────────────── 상황별 가이드 (봇 설계용) ─────────────────
# [용도 A] 거래별 체결검증(보유기간 윈도) — ★봇 TF 무관 거의 항상 브루트:
#   7h봇 보유 7~30h=420~1,800봉 / 1h봇 1~6h=60~360봉 / 15m봇 15m~2h=15~120봉 / 스윙 1달=43,200봉
#   → 전부 W*(40만) 한참 아래 = 브루트. ★짧은 TF봇일수록 보유창↓ → 브루트가 '더' 유리(통념과 반대).
#   거래수(N)가 늘어도 '윈도당' 크기는 그대로라 per-search 브루트 최적(총시간=N×작은브루트).
# [용도 B] 전기간/교차 가격탐색(한 쿼리가 큰 구간) — 캐스케이드:
#   "백테 전체에서 가격 X가 닿은 모든 시점" 류. 스캔 구간 ≥40만봉(≥~0.8년 1m)이면 캐스케이드.
#   ★'백테 기간이 늘면 달라진다'는 여기서 성립(per-trade 아님, 전기간 1쿼리일 때).
# [용도 C] 틱데이터 / RAM 초과 — 무조건 캐스케이드(속도 아닌 메모리 경계). fits_in_ram=False.
#
# 한 줄 규칙: '한 번의 탐색 윈도'가 핵심변수. <40만봉=브루트, >=40만봉 or RAM초과=캐스케이드.

if __name__ == "__main__":
    cases = [("7h봇 체결검증(보유 30h)", 1800, True),
             ("15m봇 체결검증(보유 2h)", 120, True),
             ("스윙봇 체결검증(보유 1달)", 43200, True),
             ("전기간 가격탐색(36개월 1m)", 1_578_240, True),
             ("틱데이터 전기간", 100_000_000, False)]
    for nm, w, ram in cases:
        print(f"{nm:<28} window={w:>12,}봉 ram={ram} → {pick_search_method(w, ram)}")
